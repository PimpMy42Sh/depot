#include "libft.h"

char	*ft_strdup(const char *s1)
{
	int		len;
	char	*str;

	len = ft_strlen(s1);
	str = ft_strnew(len);
	if (!str)
		return (NULL);
	ft_strcpy(str, s1);
	return (str);
}
